package cadastral;

public class Cidade {
	
	private String cidade; // cidade/class endereco > cidade
	private String estado;
	private float qt_homicidios;
	private float renda;
	
	

	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public float getQt_homicidios() {
		return qt_homicidios;
	}
	public void setQt_homicidios(float qt_homicidios) {
		this.qt_homicidios = qt_homicidios;
	}
	public float getRenda() {
		return renda;
	}
	public void setRenda(float renda) {
		this.renda = renda;
	}
	
	

}
